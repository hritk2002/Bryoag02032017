package com.bryoag.shared.util.pojo;

/**
 * The Class MapsPojo which stores each station information.
 */
public class MapsPojo {

	/** The station code. */
	private String stationCode;

	/** The station name. */
	private String stationName;

	/** The country name. */
	private String countryName;

	/** The popup content. */
	private String popupContent;

	/** The address format. */
	private String addressFormat;

	/** The swiss. */
	private boolean swiss;

	/** The lx. */
	private boolean lx;

	/** The wk. */
	private boolean wk;

	/** The interline. */
	private boolean interline;

	/** The latitude. */
	private String latitude;

	/** The langitude. */
	private String langitude;

	/** The city name. */
	private String cityName;

	/**
	 * Instantiates a new maps pojo.
	 */
	public MapsPojo() {
		super();
	}

	/**
	 * Gets the station code.
	 *
	 * @return the station code
	 */
	public String getStationCode() {
		return stationCode;
	}

	/**
	 * Sets the station code.
	 *
	 * @param stationCode the new station code
	 */
	public void setStationCode(final String stationCode) {
		this.stationCode = stationCode;
	}

	/**
	 * Gets the station name.
	 *
	 * @return the station name
	 */
	public String getStationName() {
		return stationName;
	}

	/**
	 * Sets the station name.
	 *
	 * @param stationName the new station name
	 */
	public void setStationName(final String stationName) {
		this.stationName = stationName;
	}

	/**
	 * Gets the popup content.
	 *
	 * @return the popup content
	 */
	public String getPopupContent() {
		return popupContent;
	}

	/**
	 * Sets the popup content.
	 *
	 * @param popupContent the new popup content
	 */
	public void setPopupContent(final String popupContent) {
		this.popupContent = popupContent;
	}

	/**
	 * Checks if is lx.
	 *
	 * @return true, if is lx
	 */
	public boolean isLx() {
		return lx;
	}

	/**
	 * Sets the lx.
	 *
	 * @param lx the new lx
	 */
	public void setLx(final boolean lx) {
		this.lx = lx;
	}

	/**
	 * Gets the address format.
	 *
	 * @return the address format
	 */
	public String getAddressFormat() {
		return addressFormat;
	}

	/**
	 * Sets the address format.
	 *
	 * @param addressFormat the new address format
	 */
	public void setAddressFormat(final String addressFormat) {
		this.addressFormat = addressFormat;
	}

	/**
	 * Checks if is wk.
	 *
	 * @return true, if is wk
	 */
	public boolean isWk() {
		return wk;
	}

	/**
	 * Sets the wk.
	 *
	 * @param wk the new wk
	 */
	public void setWk(final boolean wk) {
		this.wk = wk;
	}

	/**
	 * Checks if is interline.
	 *
	 * @return true, if is interline
	 */
	public boolean isInterline() {
		return interline;
	}

	/**
	 * Sets the interline.
	 *
	 * @param interline the new interline
	 */
	public void setInterline(final boolean interline) {
		this.interline = interline;
	}

	/**
	 * Gets the latitude.
	 *
	 * @return the latitude
	 */
	public String getLatitude() {
		return latitude;
	}

	/**
	 * Sets the latitude.
	 *
	 * @param latitude the new latitude
	 */
	public void setLatitude(final String latitude) {
		this.latitude = latitude;
	}

	/**
	 * Gets the langitude.
	 *
	 * @return the langitude
	 */
	public String getLangitude() {
		return langitude;
	}

	/**
	 * Sets the langitude.
	 *
	 * @param langitude the new langitude
	 */
	public void setLangitude(final String langitude) {
		this.langitude = langitude;
	}

	/**
	 * Gets the country name.
	 *
	 * @return the country name
	 */
	public String getCountryName() {
		return countryName;
	}

	/**
	 * Sets the country name.
	 *
	 * @param countryName the new country name
	 */
	public void setCountryName(final String countryName) {
		this.countryName = countryName;
	}

	/**
	 * Checks if is swiss.
	 *
	 * @return true, if is swiss
	 */
	public boolean isSwiss() {
		return swiss;
	}

	/**
	 * Sets the swiss.
	 *
	 * @param swiss the new swiss
	 */
	public void setSwiss(final boolean swiss) {
		this.swiss = swiss;
	}

	/**
	 * Gets the city name.
	 *
	 * @return the city name
	 */
	public String getCityName() {
		return cityName;
	}

	/**
	 * Sets the city name.
	 *
	 * @param cityName the new city name
	 */
	public void setCityName(final String cityName) {
		this.cityName = cityName;
	}

}
